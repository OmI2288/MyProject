package LogicalProgram;

public class PrimeNumbersUpto20 {
		
	public static void main(String[] args) {
	
		System.out.print("Prime Numbers Upto 20 are = " );
		
		for (int i = 2 ;i <= 20 ; i ++)
		{
			if (i % 2 != 0  )
			{
				System.out.print(i + " ");
			}
			
		}
		
	}
	
}
